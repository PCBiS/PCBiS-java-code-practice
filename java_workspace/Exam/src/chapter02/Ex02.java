package chapter02;

public class Ex02 {
	public static void main(String[] args) {
		// System.out.print(15);
		System.out.println(15); // 숫자 15를 문자열 출력
		System.out.println("15"); // 문자 15를 문자열로 출력
		System.out.println(10 + 5); // 숫자 10+5 연산을 한 후 문자열로 변환출력
		System.out.println("1" + 5); // 문자 1에 + 숫자5를 문자로 변환하여 붙여서 출력
		System.out.println(1 + "5"); // 숫자 1에 + 문자5를 문자로 변환하여 붙여서 출력
	}
}
