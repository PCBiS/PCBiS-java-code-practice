package ex;

public class PersonalNumInfo {
	private String name;
	private String number;
	
	public PersonalNumInfo(String name, String number) {
		this.name = name;
		this.number = number;
	}
	
	String getName() {
		return this.name;
	}
	
	String getNumber() {
		return this.number;
	}
	
}
