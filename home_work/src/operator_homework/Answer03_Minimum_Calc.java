package operator_homework;

public class Answer03_Minimum_Calc {
	public static void main(String[] args) {
		int a, b, c;
		
		a = 3+6; // a는 3+6의 결과를 대입
		System.out.println("3+6="+a); // a = 9
		b = a+9; // a + 9의 결과를 b에 대입
		System.out.println("3+6+9="+b); // b = 18
		c = b+12; // b + 12 의 결과를 c에 대입
		System.out.println("3+6+9+12="+c); // c = 30
	}
}
